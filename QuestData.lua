function Oversharer_GetQuestDataById(questId)
    return questData[questId]
end

function Oversharer_GetQuestNameByIdAndLang(questId, language)
    if questId == nil or language == nil then return nil end
    val = questData[questId][language]
    if val == nil or #val == 0 then return questData[questId]["en"]
    else return val
    end
end

function Oversharer_GetQuestDataByName(questName)
    for id, names in pairs(questData) do
        for lang, name in pairs(names) do
            if name == questName then return id, questData[id] end
        end
    end
    return nil
end
